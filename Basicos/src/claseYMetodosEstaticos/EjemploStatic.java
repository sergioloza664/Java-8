package claseYMetodosEstaticos;

	// ATRIBUTOS ESTATICOS
	// ---------------------------------------------------------------------------------
	// * Están asociados a la clase, a una instancia de ella. 
	// * Son llamados atributos estaticos.
	// * Compartidos para todas las instancias de esa clase.
	// * Pueden ser manipulados por cualquier instancia.
	// * Tambien pueden ser manipulados sin crear instancias de esa clase.

	// METODOS ESTATICOS
	// ----------------------------------------------------------------------------------------
	// * Similares a las variables estaticas (static)
	// * Se pueden invocar sin crear una instancia de esa clase.
	// * Clase.metodoEstatico(...);
	// * Para acceder a una variable estatica, necesitamos un metodo estatico.
	// * Clases con metodos auxiuliares (como por ejemplo, java.util.Arrays).

	// CONSTANTES
	// -------------------------------------------------------------------------------------
	// * Se suelen definir como estaticas
	// * static final ...
	// * No se puede modificar su valor (error).
	// * Nombre en mayúsculas, separando ppalabras con guiones bajos.
	// * Ej: static final PI = 3.141592653589793;

	// CLASES ESTATICAS
	// --------------------------------------------------------------------------------
	// * Válido para clases internas
	// * Nos permiten agrupar código
	// * Para crear una instancia de la clase interna, no necesitamos una de la clase externa,
	//   si lo hacemos desde dentro de la clase externa.