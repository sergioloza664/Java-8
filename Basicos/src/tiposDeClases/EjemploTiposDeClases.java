package tiposDeClases;

// TIPOS DE CLASES
// ---------------------------------------------------------------------------------------------------------
// Aunque Java tiene solamente una forma de crear clases, los patrones de diseño nos dicen que podemos 
// encontrar diferentes tipos de clases según su cometido:
// * Modelo: 	 Representan objetos o hechos de la naturaleza: un coche, un asiento contable, los datos
//    		 	 meteorológicos de un día. Suelen tener atributos, getters y setters, equals, hashCode, 
//				 toString, …
// * Servicios:  Implementan la lógica de negocio. Suelen tener algunos atributos, pero sobre todo métodos 
//				 públicos y privados.
// * Auxiliares: Sirven para realizar operaciones auxiliares de cálculo o transformación de datos. 
//				 Mayoritariamente, sus métodos son estáticos.
// * Main: 		 Son el punto de entrada de la aplicación. La mayoría de las ocasiones, solo tienen 
//				 este método, y si tienen más, suelen ser estáticos.
// * Test: 		 Clases orientadas a realizar pruebas de nuestra aplicación. En Java, suelen ser 
//				 test unitarios con JUnit.

